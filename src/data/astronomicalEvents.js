// Eliminar automáticamente los eventos cuya fecha ya pasó.
// La lista exportada queda ordenada por fecha (asc) para que el Home muestre primero lo más próximo.
export const astronomicalEvents = allAstronomicalEvents
  .filter((event) => isFutureDate(event.date))
  .sort((a, b) => a.date.localeCompare(b.date));

// Función para obtener eventos por tipo
export const getEventsByType = (type) => {
  return astronomicalEvents.filter((event) => event.type === type);
};

// Función para obtener eventos por dificultad
export const getEventsByDifficulty = (difficulty) => {
  return astronomicalEvents.filter((event) => event.difficulty === difficulty);
};

// Función para obtener próximos eventos (simulado)
export const getUpcomingEvents = () => {
  return astronomicalEvents.slice(0, 5);
};

